'''
bool -> boolean -> verdadeiro ou falso
Geralmente ao perguntar algo ao python ele apenas responderá verdadeiro ou falso
True (verdadeiro/SIM) ou False(falso/NÃO)
Essas “perguntas” são feitas por operadores logicos
'''
# Tipo bool(boolean)
# ao perguntar algo do python, só se pode responder
#SIM(true) ou FALSE(false)
# geralmente operadores logicos fazem esses questionamentos
# ==, operador logico que questiona a igualdade de valores
print(10 == 11) #Não = false
print(10 == 10) #Sim = true
print(type(10 == 11)) #bool
print(type(10 == 10)) #bool
print(type(True)) #bool
print(type(False)) #bool
